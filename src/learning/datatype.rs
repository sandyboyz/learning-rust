

pub fn datatype() {
    // Int Family 
    let a = 20; // i32
    println!("I32: {}", a);

    // Float Family
    let b = 3.14; // f64
    println!("f64: {}", b);

    // Boolean
    let c = true;
    println!("Bool: {}", c);
    
    // Char || Single Digit String
    let d = 'W';
    println!("Char: {}", d)
}